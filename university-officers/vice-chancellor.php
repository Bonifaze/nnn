                    <h2><span class="NormalTextRun SCXW238912313 BCX0">Professor Professor Hycinth E Ichoku</span><span class="NormalTextRun SCXW238912313 BCX0">,</span><span class="NormalTextRun SCXW238912313 BCX0"> CBE</span><span class="NormalTextRun SCXW238912313 BCX0">,</span><span class="NormalTextRun SCXW238912313 BCX0"> FRS, </span><span class="NormalTextRun SpellingErrorV2Themed SCXW238912313 BCX0">FMedSci</span></h2>
                    <p>Rev Fr Professor Hyacinth Eme Ichoku is a priest of the Catholic Diocese of Awka, Anambra State,
                      and a Professor of Development Microeconomics with primary interest in Health Economics.
                      Until his appointment as the Vice-Chancellor of Veritas University Abuja in 2018, 
                      he was a Professor at the Department of Economics at the University of Nigeria, Nsukka (UNN) where he thought Microeconomics,
                      Econometrics and Public Finance. His academic training started with degrees in Philosophy and Divinity in 1984 and 1988, respectively. 
                       After serving as the Finance Administrator in the Catholic Institute of West Africa (1989-1992) he went to study Economics at the University of Nigeria Nsukka where he obtained a BSc degree in Economics in 1998. He was awarded the Scholarship of the Government of Sweden (SIDA fellowship) to do his Master’s Degree at the University of Cape Town (South Africa) in 1999 where he graduated with Distinction in Dissertation in year 2000 and enrolled for his PhD in 2002 before he was recalled to take up teaching appointment in the Department of Economics, UNN. He combined this lecturing assignment in UNN with his PhD Research at the
                      University of Cape until he was awarded a PhD in 2006 when returned fully to Nigeria.</p>
