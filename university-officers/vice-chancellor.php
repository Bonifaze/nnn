                    <p>Rev Professor Ichoku has had a wide range of field experience from consultancies undertaking for several international organizations including World Bank, USAID, DFID, EU, WHO, IDRC, Results for Development (USA), Apt Associates among others.</p>
                    <p>He has served as PhD Examiner to University of Cape Town (South Africa), North West University (South Africa), University of Lagos, and Nnamdi Azikiwe University (Awka) among others. He led an international accreditation team to University of Technology Namibia, 
                      and was a member of international accreditation to St Augustine College, Johannesburg.</p>
                    <p>Apart from his regular teaching and research Rev Prof Ichoku also served the University of Nigeria Nsukka in several capacities and chaired over 10 Committees of the University. </p>
